import numpy
# ----------------------------------------------------------------------------------------------------------------------
def sigmoid(x):
    return 1 / (1 + numpy.exp(-x))
# ----------------------------------------------------------------------------------------------------------------------
def logit(y):
    tol = 0.00001
    return numpy.log(numpy.clip(y,tol,1-tol)) - numpy.log(1 - numpy.clip(y,tol,1-tol))
# ----------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':

    x = 10*numpy.random.rand(3,3)
    y= logit(x)
    z = sigmoid(y)

    print(x)
    print('-----------')
    print(z)